                            <div style={{ fontSize: '1.5rem', fontWeight: 'bold', color: 'var(--text-primary)' }}>{manualVitals.spo2} <span style={{ fontSize: '0.8rem', color: 'var(--text-secondary)' }}>%</span></div>
