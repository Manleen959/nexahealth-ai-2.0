            dates: allDates.length ? allDates : ['No Data'],
            symptoms: allDates.length ? allDates.map(d => symptomData[d] || 0) : [0],
            faceConfidences: allDates.length ? allDates.map(d => {
                if(!faceData[d]) return 0;
                return faceData[d].reduce((a,b)=>a+b,0) / faceData[d].length;
            }) : [0]
